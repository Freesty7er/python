def my_func(arg_1):

    if ord(min(list(arg_1))) < 97 or ord(max(list(arg_1))) > 122:
        return f'строка должна содержать маленькие английские буквы'
    else:
        return arg_1.title()

# ----------------------------------------------------------------------------------------------------------------------
my_str = input('введите слово или несколько слов маленькими английскими буквами: ')


for i,j in enumerate(list(my_str.split())):
    print(my_func(j), end=' ')